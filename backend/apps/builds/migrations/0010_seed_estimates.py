def fill_estimates(apps, schema_editor):
    Build = apps.get_model("builds", "Build")
    BuildEstimateValue = apps.get_model("builds", "BuildEstimateValue")

    for slug, values_by_order in ESTIMATES.items():
        b = Build.objects.filter(slug=slug).first()
        if b is None:
            continue
        for ev in b.estimate_values.select_related("stage").all():
            order = ev.stage.order
            if order not in values_by_order:
                continue
            # Идемпотентно: если значение уже не нулевое (заполнено
            # вручную или предыдущей миграцией) — не трогаем.
            if (Decimal(ev.materials_cost or 0) > 0
                    or Decimal(ev.works_cost or 0) > 0):
                continue
            mats, works = values_by_order[order]
            ev.materials_cost = mats
            ev.works_cost = works
            ev.save(update_fields=["materials_cost", "works_cost"])


