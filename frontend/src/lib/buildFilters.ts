export type FilterDefinition = {
  slug: FilterType;
  /** Метка над H1 в Hero-блоке страницы. */
  kicker: string;
  /** H1 страницы — он же часть title. */
  title: string;
  /** SEO Title (показывается во вкладке и в выдаче). */
  metaTitle: string;
  /** SEO Description — до ~160 символов. */
  metaDescription: string;
  /** Лид-абзац под H1 (1–3 предложения). */
  intro: string;
  /** Группа фильтра — для перелинковки «другие фильтры в этой группе». */
  group: "etazh" | "ploshchad" | "osobennosti";
  /**
   * Предикат для отбора билдов. Получает один билд из каталога,
   * возвращает true если он подходит под фильтр. Логика максимально
   * простая (этажность/площадь по числу, особенности по slug билда),
   * чтобы не плодить новые поля в БД.
   */
  matches: (b: BuildListItem) => boolean;
};

const slugContains = (slug: string, ...needles: string[]) =>
  needles.some((n) => slug.includes(n));

